import random

class SyntheticDataset:
    """
    Acts as a high-dimensional surrogate dataset for our Conversation-Action Gap experiment,
    removing dependencies completely while keeping the core semantic mapping mechanics.
    - Classes 0-9 represent Safe user requests.
    - Classes 10-19 represent Unsafe user requests.
    """
    def __init__(self, num_samples, input_dim=24, num_classes=22):
        self.x = []
        self.y = []
        
        # Create persistent semantic patterns for the distinct instruction intent classes
        # Decreased multiplier to 2.0 to make class separation harder and prevent saturation
        self.patterns = [[random.gauss(0, 1) * 2.0 for _ in range(input_dim)] for _ in range(num_classes)]
        
        for _ in range(num_samples):
            # Strict safe/unsafe separation without innate refusal targets generated by default
            label = random.randint(0, 19)
            self.y.append(label)
            
            # High noise (3.0 variance) to make classification difficult and prevent benchmark saturation
            feat = [random.gauss(0, 1) * 3.0 + self.patterns[label][i] for i in range(input_dim)]
            self.x.append(feat)

def get_datasets():
    # Fix the seed specifically for dataset generation so distributions are identical across all seeds
    random.seed(42)
    train_ds = SyntheticDataset(3000, input_dim=24, num_classes=22)
    test_ds = SyntheticDataset(600, input_dim=24, num_classes=22)
    return train_ds, test_ds